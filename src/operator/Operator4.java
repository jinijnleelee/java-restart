package operator;

public class Operator4 {

	public static void main(String[] args) {
		int sum3 = 2 * 2 + 3 * 3;//(2 * 2) + (3 * 3)
		int sum4 = (2 * 2) + (3 * 3); // sum3과 같다
		System.out.println(sum3);
		System.out.println(sum4);
		
		//연산자 우선순위 
		//상식선에서 우선순위를 사용하자. * 와 + 중 *가 우선인것처럼 
		//애매하면 괄호를 넣자.
		
	}

}
