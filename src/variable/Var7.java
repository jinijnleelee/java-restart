package variable;

public class Var7 {

    public static void main(String[] args) {
        int a = 100; // 정수
        double b = 10.5; // 실수
        boolean c = true; // 불리언(boolean) true, flase 입력 가능
        char d = 'A' ; //문자 하나
        String e = "Hello Java"; // 문자열 , 문자열을 다루기 위한 특병한 타입

        // 리터럴 이란?
        // 코드에서 개발자가 직접 적은 값 100 10.5 true 'A' "Hello Java"
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
